package Java.testing;

public class Armor {
    private String name;
    private int defense;

    // Constructor
    public Armor(String name, int defense) {
        this.name = name;
        this.defense = defense;
    }

    // Getters and setters
    public String getName() {
        return name;
    }

    public int getDefense() {
        return defense;
    }
}
